!==============================================================================!
  function intensity_det(x_int)
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  real(kind=8) :: intensity_det
  real(kind=8) :: x_int
!==============================================================================!

  if ( x_int > 0 ) then
    intensity_det = 1
  else
    intensity_det = -1
  end if

  end function
