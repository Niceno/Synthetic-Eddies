!==============================================================================!
  subroutine Mat_Mul(a, b, ab, ni, nj, nk)
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  integer      :: ni,nj,nk
  real(kind=8) :: ab(ni,nj)
  real(kind=8) :: a(ni,nk),b(nk,nj)
!-----------------------------------[Locals]-----------------------------------!
  integer :: i,j,k
!==============================================================================!

  ab(:,:) = 0.0

  do i = 1,ni
    do j = 1,nj
      do k = 1,nk
        ab(i,j) = ab(i,j) + a(i,k) * b(k,j)
      end do
    end do
  end do

  end subroutine
